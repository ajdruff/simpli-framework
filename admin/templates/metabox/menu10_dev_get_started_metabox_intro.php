<h4>What is the Nomstock Com Plugin?</h4>
<p>The Nomstock Com plugin is both a demonstration plugin for the Simpli Framework, and the Simpli Framework itself. For more information on the Simpli Framework, go to <a href="http://simpliwp.com/framework/">http://simpliwp.com/framework/</a></p>
<h4>What does Nomstock Com do ?</h4>
<p>As a plugin, Nomstock Com inserts customizable text at the start or end of any post you create. The default text is 'Hello World'.</p>
<p>Nomstock Com can also be used as a template to create your own plugins built on the Simpli Framework. See the 'How do I get started with the Simpli Framework?' for more information.</p>
<h4>How do I use Nomstock Com ?</h4>
<ol>
<li>Go to General -&gt; Hello Settings</li>
<li>The settings here will become the defaults for any post that has Nomstock Com enabled.</li>
<li>Configure the Text Placement and Hello World Text. </li>
<li>Next, create a post or edit an existing post.</li>
<li>Under 'Nomstock Com' settings within the post editor, select 'enabled' for 'Text Insertion Enabled' and change 'placement' or 'text' if you want them to be different from the defaults. Publish or update your post.</li>
<li>Now view your post. You should see either the global default text or the text configured in the post settings display either before or after your post content, depending on your settings.</li>
</ol>
<h4>Whats the big deal?</h4>
<p>If you're a plugin developer, using the Nomstock Com plugin to create your next plugin will help you rapidly create an object oriented WordPress plugin built on the Simpli Framework.</p>
<h4>How do I get started with the Simpli Framework?</h4>
<p>To get started with the Simpli Framework, you'll need access to a bash shell to run the make script that creates your plugin. </p>
<ol>
<li>Open the bash shell</li>
<li>change the directory where the simpli-framework directory is , containing the Nomstock Com plugin.</li>
<li>Run the following command :
./make.sh</li>
<li>Follow the prompts.</li>
<li>Or you can run it without prompts by entering something like this : </li>
<li>./make.sh mycompany_myplugin ../
</li>
<li>That command will build a plugin called 'Mycompany Myplugin' and place it in the directory  wp-content/mycompany-myplugin. </li>
<li>After making the new plugin, you can go ahead and start adding code to implement its features. For more help, see http://simpliwp.com/framework/</li>
</ol>

